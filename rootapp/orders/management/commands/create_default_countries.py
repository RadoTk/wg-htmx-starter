class Command(BaseCommand):
    help = "Crée tous les pays par défaut dans le modèle Country"

    def handle(self, *args, **kwargs):
        created_count = 0
        for code, name in COUNTRY_CHOICES:
            country, created = Country.objects.get_or_create(
                name=name,
                defaults={"code": code}
            )
            if created:
                created_count += 1
                self.stdout.write(self.style.SUCCESS(f"Ajouté: {name}"))
            else:
                self.stdout.write(f"Déjà existant: {name}")
        
        self.stdout.write(self.style.SUCCESS(f"{created_count} pays créés."))
